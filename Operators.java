package com.company;

public class Operators {
    public static void main(String[] args) {
        //arithmetic
        int a=4;
        int b=9;
        //assignment
        b*=a;
        //comparison
        System.out.println(b);
        System.out.println(6==9);
        //Logical
        System.out.println(65>9 && 56>99);
        System.out.println(65>9 || 56>99);
        //bitwise
        System.out.println(2|3);
        System.out.println(2&3);

    }
}
