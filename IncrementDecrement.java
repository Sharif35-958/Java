package com.company;

public class IncrementDecrement {
    public static void main(String[] args) {
        int i=55;
        System.out.println(i++);
        System.out.println(i);
        System.out.println(++i);
        System.out.println(i);
        int j=40;
        System.out.println(j--);
        System.out.println(j);
        System.out.println(j++);
        System.out.println(j);
        System.out.println(--j);
        char ch= 'a';
        System.out.println(++ch);


    }
}
